# python string method 
# lower():-it convert every single character to lower. 
#upper():-it convert every character to upper case letter.
#capitalize():-it converts first character to upper case.
#title():-it converts the first character each word to upper case.
#swapcase():-it simply swap the lower case to uppercase and vice versa...
x="Hey this is the abhishek kumar"
y="I am from bihar"
print(x.lower())
print(y.upper())
print(x.capitalize())
print(y.title())
print(x.swapcase())
