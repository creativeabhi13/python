#boolean int to bool conversion

a=bool(10)
b=bool(0)
c=bool(-10)

print(a,type(a))
print(b,type(b))
print(c,type(c))

# float to boolean
a=bool(10.6)
b=bool(0.0)
c=bool(-10.9)

print(a,type(a))
print(b,type(b))
print(c,type(c))

#string to boolean conversion
a=bool('10')
b=bool('india')
c=bool('-10')
d=bool("")
e=bool(" ")

print(a,type(a))
print(b,type(b))
print(c,type(c))
print(d,type(d))
print(e,type(e))